class CustomerInsertBody extends StatefulWidget {
  final Size deviceSize;
  final Widget profileImage;
  final String heroTag;
  final String title;

  CustomerInsertBody({
    Key key,
    @required this.deviceSize,
    this.profileImage,
    this.title,
    this.heroTag,
  }) : super(key: key);

  @override
  _CustomerInsertBodyState createState() => _CustomerInsertBodyState();
}

